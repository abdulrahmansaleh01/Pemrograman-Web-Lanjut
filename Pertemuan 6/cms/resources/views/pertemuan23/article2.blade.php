        <h2>PENGERTIAN LARAVEL</h2>
        <p class="lead" align="justify">Jika Anda sudah tahu pemrograman PHP, maka Laravel ini adalah salah satu framework 
            PHP yang bisa membantu Anda dalam menggunakan PHP lebih mudah, cepat dan efisien dalam proses develop website. 
            PHP menjadi bahasa pemrograman yang paling dinamis, apalagi dengan adanya update rilis PHP versi terbaru. 
            Saat ini Laravel juga tidak mau ketinggalan, biasanya framework ini akan melakukan update dan memunculkan teknologi baru juga.
            Framework Laravel lebih fokus pada bagian end-user dengan memastikan saat membuat website bisa dilakukan lebih sederhana baik dalam 
            hal penulisan script maupun tampilan sehingga bisa menghasilkan website sebagaimana mestinya. Ini semua tidak akan bisa Anda temui saat menggunakan native PHP,
            jadi untuk keputusan menggunakan framework PHP adalah tepat.</p>

        <h2>SEJARAH LARAVEL</h2>
        <p class="lead" align="justify"> Menurut sejarahnya, Laravel pertama kali diluncurkan sudah sejak bulan Juni 2011. Tidak butuh waktu yang lama framework yang dikembangkan oleh Taylor Otwell ini bisa menjadi framework terpopuler di dunia, tepatnya bulan Desember 2013. Laravel menjadi 
            framework yang mendapatkan bintang paling banyak pada social media untuk para developer yaitu Github. 
            Bahkan sekarang ini Laravel juga menjadi salah satu framework PHP yang populer di Indonesia.</p>
